def fetch_tamil_news(claim, max_items_per_source: int = 25):
    """
    Fetch Tamil news headlines via RSS (no API keys).
    Uses: requests + feedparser
    Returns: [{"title": "...", "source": "BBC Tamil"}, ...]
    """
    try:
        import feedparser
    except Exception as e:
        raise ImportError("Please install feedparser: pip install feedparser") from e

    try:
        import requests
    except Exception as e:
        raise ImportError("Please install requests: pip install requests") from e

    items: List[Dict[str, str]] = []

    def _parse(url: str):
        parsed = feedparser.parse(url)
        if getattr(parsed, "entries", None):
            return parsed
        try:
            resp = requests.get(url, timeout=15, headers={"User-Agent": "Mozilla/5.0"})
            resp.raise_for_status()
            return feedparser.parse(resp.content)
        except Exception:
            return parsed

    for source_name, url in RSS_SOURCES:
        parsed = _parse(url)
        for entry in getattr(parsed, "entries", [])[:max_items_per_source]:
            title = (entry.get("title") or "").strip()
            if title:
                items.append({"title": title, "source": source_name})

    seen = set()
    out = []
    for it in items:
        k = (it["title"], it["source"])
        if k not in seen:
            out.append(it)
            seen.add(k)
    return out


