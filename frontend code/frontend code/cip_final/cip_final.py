def extract_tamil_keywords(text) -> Dict[str, List[str]]:
    """
    Extract:
    - entities (salient tokens)
    - event terms
    - numbers (esp. years/dates)
    """
    raw = str(text or "")
    cleaned = preprocess_tamil_text(raw, tamil_stopwords)
    tokens = [t for t in cleaned.split() if len(t) >= 2]

    years = sorted(set(_YEAR_RE.findall(raw)))
    dates = sorted(set(_DATE_RE.findall(raw)))
    numbers = sorted(set(_NUMBER_RE.findall(raw)))

    lower_raw = raw.lower()
    events = [t for t in _EVENT_TERMS if t in lower_raw]

    uniq = sorted(set(tokens), key=lambda x: (-len(x), x))
    entities = uniq[:12]

    return {"entities": entities, "events": sorted(set(events)), "years": years, "dates": dates, "numbers": numbers}


def _token_set_for_match(text: str) -> set:
    cleaned = preprocess_tamil_text(text, tamil_stopwords)
    return set(t for t in cleaned.split() if len(t) >= 2)


def match_and_validate(claim, news_list, top_n_evidence: int = 5):
    """
    STEP 1: Keyword matching (overlap)
    STEP 2: Fact checking (event + entities + YEAR)

    Returns: (status, evidence_list)
    """
    if not claim or not news_list:
        return "No Match", []

    claim_kw = extract_tamil_keywords(claim)
    claim_tokens = _token_set_for_match(claim)
    claim_years = set(claim_kw["years"])
    claim_events = set(claim_kw["events"])
    claim_entities = set(claim_kw["entities"])

    scored = []
    for item in news_list:
        title = item.get("title", "")
        title_kw = extract_tamil_keywords(title)
        title_tokens = _token_set_for_match(title)
        overlap = claim_tokens.intersection(title_tokens)
        score = len(overlap) / max(1, len(claim_tokens))

        if claim_years and set(title_kw["years"]).intersection(claim_years):
            score += 0.25
        if claim_events and set(title_kw["events"]).intersection(claim_events):
            score += 0.15
        if claim_entities and set(title_kw["entities"]).intersection(claim_entities):
            score += 0.10

        scored.append((score, item, title_kw))

    scored.sort(key=lambda x: x[0], reverse=True)
    best = scored[: max(1, top_n_evidence)]
    evidence = [b[1] for b in best if b[0] > 0]

    if not evidence or best[0][0] < 0.15:
        return "No Match", []

    top_score, top_item, top_kw = best[0]
    top_years = set(top_kw["years"])
    top_events = set(top_kw["events"])
    top_entities = set(top_kw["entities"])

    event_match = bool(claim_events and top_events and claim_events.intersection(top_events))
    entity_match = bool(claim_entities and top_entities and claim_entities.intersection(top_entities))

    if claim_years:
        year_match = bool(top_years.intersection(claim_years))
        if event_match and entity_match and year_match:
            return "Verified", evidence[:top_n_evidence]
        if event_match and not year_match:
            return "Contradiction", evidence[:top_n_evidence]
        return "Partial", evidence[:top_n_evidence]

    if event_match and entity_match:
        return "Verified", evidence[:top_n_evidence]
    if top_score >= 0.30 or event_match or entity_match:
        return "Partial", evidence[:top_n_evidence]
    return "No Match", []


def verify_online_sources(claim):
    news_list = fetch_tamil_news(claim)
    status, evidence = match_and_validate(claim, news_list)

    if status == "Verified":
        return "Verified from Tamil trusted sources", "REAL", evidence
    elif status == "Contradiction":
        return "False (mismatched facts like date/year)", "FAKE", evidence
    elif status == "Partial":
        return "Misleading / partially true", "FAKE", evidence
    else:
        return "No strong evidence found", "FAKE", evidence


def enhanced_pipeline(text):
    """
    Flow:
    Input -> existing preprocessing -> existing BiLSTM prediction -> claim extraction -> LIME ->
    RSS fetch -> match + validate -> override -> output
    """
    model_label, confidence = predict_fake_news(text)
    model_prediction = "REAL" if model_label == "Real" else "FAKE" if model_label == "Fake" else "Unknown"

    claim = extract_claim(text)
    important_words = explain_prediction(text, loaded_model)
    verification_result, verification_label, evidence = verify_online_sources(claim)

    final_prediction = model_prediction
    if model_prediction == "REAL" and verification_label == "FAKE":
        final_prediction = "FAKE"

    return (
        model_prediction,
        final_prediction,
        float(confidence),
        claim,
        important_words,
        verification_result,
        evidence,
    )


