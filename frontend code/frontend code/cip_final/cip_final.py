        if not news_text.strip():
            st.warning("Please enter Tamil news text.")
            st.stop()

        (
            model_prediction,
            final_prediction,
            confidence,
            extracted_claim,
            important_words,
            verification_result,
            evidence,
        ) = enhanced_pipeline(news_text)

        st.subheader("Input News")
        st.write(news_text)

        st.subheader("Extracted Claim")
        st.write(extracted_claim)

        st.subheader("Model Prediction")
        st.write(model_prediction)

        st.subheader("Final Verified Decision")
        if final_prediction == "REAL":
            st.success("REAL")
        else:
            st.error("FAKE")

        st.subheader("Confidence Score")
        st.write(f"{confidence:.1f}%")

        st.subheader("Important Words (Explainable AI)")
        st.table([{"word": w, "weight": float(weight)} for w, weight in important_words])

        st.subheader("Verification Result")
        st.write(verification_result)

        st.subheader("Supporting Evidence")
        if evidence:
            for e in evidence:
                st.write(f'- {e["title"]} ({e["source"]})')
        else:
            st.write("No matching headlines found from RSS sources.")